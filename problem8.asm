	;; print format
	fmt db "max prod is %lld",10,0
	num equ 13
	
	section .bss
	prod resq 1
	max  resq 1
	section .text
	global main
main:	
	push rsp
	mov rbp, rsp
	mov rcx, 0


	mov qword [max], 0
mul_loop:
	mov qword [prod], 1	
	cmp rcx, 1000 - num
	jge endl
	lea r8, matix
	add r8, rcx
	xor r9, r9
in_loop:
	cmp r9, num
	jge next_start
	xor rax,rax
	mov al, byte [r9+r8]
	sub al, byte  "0"
	imul qword [prod]
	mov [prod], rax
	inc r9
	jmp in_loop

next_start:
	mov rax,[prod]
	cmp rax, [max]
	jle next_i
	mov [max], rax
next_i:	
	inc rcx
	jmp mul_loop

endl:
	mov rdi, fmt
	mov rsi, qword [max]
	mov rax, 0
	call printf
	leave
	ret
